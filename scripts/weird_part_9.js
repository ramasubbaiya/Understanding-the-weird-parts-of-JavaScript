/** 
 *  DYNAMIC TYPING
 *      You don't tell the engine what type of Data a variable Holds, 
 *      It Figures it out while your is running
 *  
 *      Variables can hold different types of values because 
 *      it's all figured out during execution
 * 
 * 
 *      Static Typing 
 *          bool isAvail = 'No'; // error
 *          // this isAvail data type cannot be changed once declared, it is boolean forever.
 *      Dynamic Typing
 *          var isAvail = true;  // no errors
 *          isAvail = 'Yeah';
 *          isAvail = 1;
 * 
 *      Javascript is Dynamic Typing programming language
 * 
 * 
 * 
 * 
 * 
 *      PRIMITIVE TYPE
 *          A type of Data that represents a Single value
 *              That is, not an Object
 * 
 *     undefined    -       represents lack of existence
 *                          (you shouldn't set a variable to this)
 * 
 *     null         -       represents lack of existence
 *                          (you can set a variable to this)
 * 
 *     boolean      -       true or false 
 * 
 *     number       -       Floating point number (there's always some decimals). 
 *                          Unlike other programming languages, there's only one 'number' type...
 *                          and it can make math weird
 *  
 *     string       -       a sequence of characters 
 *                          (both '' and "" can be used)
 * 
 *     symbol
 *     (new - ES6)  -       Used in next version of javascript (at this time of documentation)
 *          
 */